    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

    if (txtRecepcion.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "ID Recepción es obligatorio");
        txtRecepcion.requestFocus();
        return;
    }

    File archivo = new File("datos/recepcion.txt");
    boolean encontrado = false;

    try {
        if (archivo.exists()) {
            BufferedReader br = new BufferedReader(new FileReader(archivo));
            String linea;

            while ((linea = br.readLine()) != null) {
                String[] datos = linea.split("\\|");

                if (datos[0].equals(txtRecepcion.getText())) {

                    txtMatricula.setText(datos[1]);
                    SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                    Date fecha = sdf.parse(datos[2]);
                    txtFecha.setDate(fecha);
                    txtObservacion.setText(datos[3]);

                    lblEstado.setText("Modificando...");
                    encontrado = true;
                    break;
                }
            }
            br.close();
        }

        if (!encontrado) {
            lblEstado.setText("Creando...");
            txtMatricula.setText("");
            txtFecha.setDate(null);
            txtObservacion.setText("");

            JOptionPane.showMessageDialog(
                this,
                "La recepción no existe.\nPuede crear una nueva."
            );
        }

        txtMatricula.setEditable(true);
        txtFecha.setEnabled(true);
        txtObservacion.setEditable(true);

        txtRecepcion.setEditable(false);
        btnBuscar.setEnabled(false);
        btnGuardar.setEnabled(true);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al buscar recepción");
    }
 // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarActionPerformed

