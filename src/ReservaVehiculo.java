    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
     
    if (txtRecepcion.getText().trim().isEmpty() ||
        txtMatricula.getText().trim().isEmpty() ||
        txtFecha.getDate() == null) {

        JOptionPane.showMessageDialog(this,
            "Id Recepción, Matrícula y Fecha son obligatorios");
        return;
    }

    
    
    Date fechaRecepcion = txtFecha.getDate();
    SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
    String fechaTexto = sdf.format(fechaRecepcion);

    
    File archivoReservas = new File("datos/reservas.txt");
    Date fechaEntrada = null;

    try {
        if (archivoReservas.exists()) {
            BufferedReader br = new BufferedReader(new FileReader(archivoReservas));
            String linea;

            while ((linea = br.readLine()) != null) {
                String[] datos = linea.split("\\|");

                
                if (datos.length >= 3 && datos[1].equals(txtMatricula.getText())) {
                    fechaEntrada = sdf.parse(datos[2]);
                    break;
                }
            }
            br.close();
        }

        if (fechaEntrada != null && fechaRecepcion.before(fechaEntrada)) {
            JOptionPane.showMessageDialog(this,
                "La fecha de recepción no puede ser menor\n" +
                "que la fecha de entrada en reservas");
            return;
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,
            "Error validando la fecha con reservas");
        return;
    }

   
    String observacion = txtObservacion.getText().trim();

    
    File archivo = new File("datos/recepcion.txt");
    archivo.getParentFile().mkdirs();

    try {
        ArrayList<String> lista = new ArrayList<>();
        boolean actualizado = false;

        if (archivo.exists()) {
            BufferedReader br = new BufferedReader(new FileReader(archivo));
            String linea;

            while ((linea = br.readLine()) != null) {
                String[] datos = linea.split(";");

                if (datos[0].equals(txtRecepcion.getText())) {

                    String nuevaLinea =
                        txtRecepcion.getText() + ";" +
                        txtMatricula.getText() + ";" +
                        fechaTexto + ";" +
                        observacion;

                    lista.add(nuevaLinea);
                    actualizado = true;
                } else {
                    lista.add(linea);
                }
            }
            br.close();
        }

        if (!actualizado) {
            String nuevaLinea =
                txtRecepcion.getText() + ";" +
                txtMatricula.getText() + ";" +
                fechaTexto + ";" +
                observacion;

            lista.add(nuevaLinea);
        }

        BufferedWriter bw = new BufferedWriter(new FileWriter(archivo));
        for (String l : lista) {
            bw.write(l);
            bw.newLine();
        }
        bw.close();

        JOptionPane.showMessageDialog(this,
            actualizado ? "Recepción modificada correctamente"
                        : "Recepción creada correctamente");

        limpiarFormulario();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al guardar recepción");
        e.printStackTrace();
    }

  // TODO add your handling code here:
    }//GEN-LAST:event_btnGuardarActionPerformed
